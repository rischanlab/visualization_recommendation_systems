package edu.uiuc.zenvisage.model;

public class Link {
	public int source;
	public int target;
	
	public Link() {
		
	}

	public int getSource() {
		return source;
	}

	public void setSource(int source) {
		this.source = source;
	}

	public int getTarget() {
		return target;
	}

	public void setTarget(int target) {
		this.target = target;
	}
	
	
}
